package enumclass;

/**
 * This enum class stores all possible request type
 * @author Dennis Chen
 * @version 1.0
 *
 */
public enum RequestType {
	/**
	 * register fyp request type
	 */
	REGISTER_FYP,
	
	/**
	 * change title request type
	 */
	CHANGE_TITLE,
	
	/**
	 * deregister fyp request type
	 */
	DEREGISTER_FYP,
	
	/**
	 * transfer student request type
	 */
	TRANSFER_STUDENT,
}
