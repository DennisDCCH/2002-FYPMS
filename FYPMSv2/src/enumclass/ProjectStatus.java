package enumclass;

/**
 * This enum class stores all possible project status
 * @author Dennis Chen
 * @version 1.0
 *
 */
public enum ProjectStatus {
	/**
	 * available project status
	 */
	AVAILABLE,
	
	/**
	 * reserved project status
	 */
	RESERVED,
	
	/**
	 * unavailable project status
	 */
	UNAVAILABLE,
	
	/**
	 * allocated project status
	 */
	ALLOCATED,
}
